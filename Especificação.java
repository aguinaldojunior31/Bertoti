public class Especificação {

    private String rua;
    private String comodo;
    private String bairro;

    public Especificação(String rua, String comodo, String bairro) {
        this.rua = rua;
        this.comodo = comodo;
        this.bairro = bairro;
    }

    public getRua(){
        return rua;
    }

    public String setRua() {
        return rua;
    }

    public getcomodo(){
        return comodo;
    }

    public String setcomodo() {
        return comodo;
    }

    public getbairro(){
        return bairro;
    }

    public String bairro() {
        return bairro;
    }

}
